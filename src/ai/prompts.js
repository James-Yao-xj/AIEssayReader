/**
 * 任务级：批判性质疑。
 */
export const CRITIQUE = `# 当前任务：对论文提出批判性质疑

请从以下维度对论文提出**有理有据**的质疑。每条质疑必须能落到论文具体内容上，泛泛而谈视为不合格。

## 方法层面的质疑
- 实验设计是否合理？对照组、消融、样本量是否充分？
- 假设是否过强？哪些前提可能在实际场景下不成立？

## 结果层面的质疑
- 数据/图表是否真的支撑结论？有没有被忽略的反例？
- 评价指标是否合适？是否存在过拟合、cherry-pick？

## 论证层面的质疑
- 关键推理是否严密？有无逻辑跳跃或循环论证？
- 引用是否准确？有无夸大与已有工作的差异？

## 改进建议
列出 3~5 条具体可执行的改进方向。

**每条质疑的输出格式（必须）**：
- **质疑点**：一句话陈述问题
- **依据**：论文中的具体段落/数据，或外部公认原理
- **建议**：可执行的改进方向

不要使用未经审视的比喻来"打比方说明问题"；解释任何专业术语时遵循 GLOBAL_STYLE 的双重解释规则。`;

